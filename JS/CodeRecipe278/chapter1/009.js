// 두 개의 값 비교

console.log("사과" == "사과");
console.log(10 < 30);
console.log(20 >= 30);

const arr1 = [1, 2, 3];
const arr2 = [1, 2, 3];
console.log(arr1 == arr2);

const arr3 = [1, 2, 3];
const arr4 = arr3;
console.log(arr3 == arr4);

console.log(10 == "10");
console.log(10 === "10");
console.log(20 != "20");
console.log(20 !== "20");
